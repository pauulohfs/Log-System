    private void cxtCEPFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cxtCEPFocusLost

        try {
            String cep = cxtCEP.getText();

            Endereco ender = FuncoesUteis.consultarCEP(cep);
            if (ender != null) {
                cxtRua.setText(ender.getLogradouro());
                cxtBairro.setText(ender.getBairro());
                cxtCidade.setText(ender.getCidade());
                //cmbCidade.setSelectedItem(ender.getCidade());
            } else {
                JOptionPane.showMessageDialog(this, "CEP não encontrado.", "Cadastro de Cliente", JOptionPane.ERROR_MESSAGE);
                cxtRua.setText("");
                cxtCidade.setText("");
                cxtBairro.setText("");
                //txtRef.setText("");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex, "Cadastro de Cliente", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cxtCEPFocusLost
    private boolean validarCampos() {
        int invalidos = 0;
        txtNome.setForeground(Color.black);
        txtData.setForeground(Color.black);
        txtCPF.setForeground(Color.black);
        txtCelular.setForeground(Color.black);
        txtCEP.setForeground(Color.black);
        txtBairro.setForeground(Color.black);
        txtCidade.setForeground(Color.black);
        txtRua.setForeground(Color.black);
        txtN.setForeground(Color.black);
        txtEstadoCivil.setForeground(Color.black);
        txtPlano.setForeground(Color.black);

        if (cxtNome.getText().trim().isEmpty()) {
            invalidos++;
            txtNome.setForeground(Color.red);

        }
        if (cxtData.getValue() == null) {
            invalidos++;
            txtData.setForeground(Color.red);

        }
        if (cxtCelular.getValue() == null) {
            invalidos++;
            txtCelular.setForeground(Color.red);

        }
        if (cxtCPF.getValue() == null) {
            invalidos++;
            txtCPF.setForeground(Color.red);

        }
        if (cxtCEP.getValue() == null) {
            invalidos++;
            txtCEP.setForeground(Color.red);

        }
        if (cxtPlano.getText().isEmpty()) {
            invalidos++;
            txtPlano.setForeground(Color.red);

        }
        if (cxtBairro.getText().isEmpty()) {
            invalidos++;
            txtBairro.setForeground(Color.red);

        }
        if (cxtN.getText().isEmpty()) {
            invalidos++;
            txtN.setForeground(Color.red);

        }
        if (cxtRua.getText().isEmpty()) {
            invalidos++;
            txtRua.setForeground(Color.red);

        }
        if (cxtCidade.getText().isEmpty()) {
            invalidos++;
            txtCidade.setForeground(Color.red);

        }
        if(grpEstadoCivil.isSelected(null)){
            invalidos++;
            txtEstadoCivil.setForeground(Color.red);
        
        }
        if (invalidos == 0) {
            return true;
        } else {
            return false;
        }
    }

